package com.kkb2;

/**
 * 为什么要学习方法
 * 代表一个功能, 想什么时候用都可以
 */
public class Demo02 {
    public static void main(String[] args) {

        //执行方法
        printMethod();
        System.out.println("-----------cdode..---");

        //执行方法
        printMethod();
        System.out.println("-----------cdode..---");

        //执行方法
        printMethod();
    }

    //定义一个方法, 用来输出 "好好学习 天天向上"
    public static void printMethod(){
        System.out.println("好好学习 ~~");
        System.out.println("天天向上~~");
        System.out.println("好好学习 ~~");
        System.out.println("天天向上~~");
    }

}
