package com.kkb;

/**
 * 演示第一个格式的 if语句
 */
public class IfDemo {
    public static void main(String[] args) {

        System.out.println("开始....");

        //定义二个变量
        int a = 10;
        int b = 20;

        //判断a与b 是否相等
        if(a == b){
            //语句体
            System.out.println("a的值 小于 b的值");
        }


        System.out.println("结束....");

    }
}
